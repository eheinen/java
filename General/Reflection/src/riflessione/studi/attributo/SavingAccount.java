package riflessione.studi.attributo;

public class SavingAccount extends Account {

	private String compleanno;
	private Integer interesse;
	
	public String getCompleanno() {
		return compleanno;
	}

	public void setCompleanno(String compleanno) {
		this.compleanno = compleanno;
	}

	public Integer getInteresse() {
		return interesse;
	}

	public void setInteresse(Integer interesse) {
		this.interesse = interesse;
	}
	
}
