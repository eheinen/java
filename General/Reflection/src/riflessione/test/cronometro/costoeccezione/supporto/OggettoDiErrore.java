package riflessione.test.cronometro.costoeccezione.supporto;

public class OggettoDiErrore {

	private String chiave;
	private String valore;

	public OggettoDiErrore(String chiave, String valore) {
		this.chiave = chiave;
		this.valore = valore;
	}

	public String getChiave() {
		return chiave;
	}
	
	public String getValore() {
		return valore;
	}
	
}
